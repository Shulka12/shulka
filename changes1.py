import math

def sum_of_two_numbers(a, b):
    return a + b

# Example usage
result = sum_of_two_numbers(3, 5)
print("The sum is:", result)


def area_of_circle(radius):
    return math.pi * radius ** 2

# Example usage
circle_area = area_of_circle(5)
print("The area of the circle is:", circle_area)